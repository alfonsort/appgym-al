export function getTodayWorkoutId(date = new Date()) {
  const day = date.getDay()
  const map = {
    1: 'lunes',
    2: 'martes',
    3: 'miercoles',
    4: 'jueves',
    5: 'viernes',
  }

  return map[day] || 'lunes'
}
